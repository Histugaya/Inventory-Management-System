    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String username = usernametxt.getText();
        String password = new String(passwordTxt.getPassword());
        if (new ConnectionFactory().checkLogin(username, password)) {
            inventory id = new inventory();
            id.setResizable(false);
            id.setVisible(true);
            Toolkit thekit = id.getToolkit();//to get the full screen
            Dimension dim = thekit.getScreenSize();
            id.setSize(dim);
            dispose();
            //JOptionPane.showMessageDialog(null, "login success");
        } else {
            JOptionPane.showMessageDialog(null, "Unauthorized user");
            logindialog id = new logindialog();
            id.setLocationRelativeTo(null);
            id.setResizable(false);
            id.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

